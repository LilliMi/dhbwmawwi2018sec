package kapitel3;

public class TypeConversion {

    public static void main(String[] args){

        double numberA = 3.16;

        int numberB;

        numberB = (int)numberA;

        System.out.println(numberB);


        byte numberC;

        numberB = 128;

        numberC = (byte)numberB;

        System.out.println(numberC);

    }

}
